package com.deyatech.admin.config;

/**
 * @author doukang
 * @description 控件长度
 * @date 2019/9/10 11:10
 */
public class ControlLength {

    private String id;

    private String name;

    private int value;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
